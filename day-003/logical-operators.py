'''
Logical Operators

A and B
C or D
not E
'''
a = 12
print("a = ", a)
print("a > 15", a > 15)
print("a > 10", a > 10)
print("a > 10 and a < 13", a > 10 and a < 13)
print("a > 15 and a < 13", a > 15 and a < 13)

print("a > 10 or a < 13", a > 10 or a < 13)
print("a > 15 or a < 13", a > 15 or a < 13)

print("not a > 15", not a > 15)
